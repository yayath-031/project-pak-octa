def cari_novel(kriteria, nilai, batas):
    hasil = []
    hitung = 0
    
    for item in novel:
        if hitung >= batas:
            break  # Hentikan jika sudah mencapai batas
        
        # Sesuaikan pengecekan kriteria dengan dataset
        if isinstance(item.get(kriteria.lower()), list):  # Untuk kriteria yang berupa list (genre)
            if nilai.lower() in [x.lower() for x in item.get(kriteria.lower(), [])]:  
                hasil.append(f'{item["judul"]} - {item["penulis"]} ({", ".join(item["genre"])}) ({item["tahun terbit"]})')
                hitung += 1
        else:  # Untuk kriteria yang berupa string (tahun terbit atau penulis)
            if nilai.lower() == item.get(kriteria.lower(), "").lower():
                hasil.append(f'{item["judul"]} - {item["penulis"]} ({", ".join(item["genre"])}) ({item["tahun terbit"]})')
                hitung += 1
    
    if hasil:
        return "\n".join(hasil)
    else:
        return f'Tidak ada novel yang ditemukan dengan {kriteria} "{nilai}".'