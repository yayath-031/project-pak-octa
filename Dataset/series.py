def tarik_data_berdasarkan_kriteria(kriteria, nilai, batas):
    hasil = []
    hitung = 0
    
    for item in series:  # 'series' adalah daftar JSON yang berisi data
        if hitung >= batas:
            break  # Hentikan jika sudah mencapai batas
        # Periksa apakah kriteria ada dan merupakan list
        if isinstance(item.get(kriteria), list) and nilai.lower() in [x.lower() for x in item[kriteria]]:
            hasil.append(f'{item["judul"]} ({", ".join(item["genre"])}) ({item["tahun terbit"]}) ({item["bahasa"]})')
            hitung += 1  # Tambah penghitung jika data dengan kriteria yang diinginkan ditemukan
    
    if hasil:
        return "\n".join(hasil)
    else:
        return f'Tidak ada data yang ditemukan dengan {kriteria} "{nilai}".' 

